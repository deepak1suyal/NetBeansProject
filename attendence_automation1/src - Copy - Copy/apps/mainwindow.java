    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        // Change Password:
        try{
            String opswd=jTextField15.getText();
            String npswd=jTextField16.getText();
            String cpswd=jTextField17.getText();
            DBCon db=new DBCon();
            db.pstmt=db.con.prepareStatement("select * from login where user=? and password=?");
            db.pstmt.setString(1,user);
            db.pstmt.setString(2,opswd);
            db.rst=db.pstmt.executeQuery();
            if(db.rst.next())
            {
                if(npswd.equals(cpswd))
                {
                    db.pstmt=db.con.prepareStatement("update login set password=? where user=?");
                    db.pstmt.setString(1,npswd);
                    db.pstmt.setString(2,user);
                    int i=db.pstmt.executeUpdate();
                    if(i>0)
                    {
                        javax.swing.JOptionPane.showMessageDialog(this,"Password Updated Successfully");
                        ChangePassword.setVisible(false);
                    }
                }
  else
                {
                    javax.swing.JOptionPane.showMessageDialog(this,"New and Congirm Password does not match");
                }
                }
            else{
                javax.swing.JOptionPane.showMessageDialog(this,"Old Password does not match");
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
}
        
    }//GEN-LAST:event_jButton18ActionPerformed

